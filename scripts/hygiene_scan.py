@dataclass(frozen=True)
class Finding:
    path: Path
    line_no: int
    kind: str
    match: str
    line_hash: str

    def baseline_key(self, root: Path = DEFAULT_ROOT) -> str:
        rel = self.path.relative_to(root).as_posix()
        raw = f"{rel}:{self.line_no}:{self.kind}:{self.line_hash}"
        return hashlib.sha256(raw.encode("utf-8")).hexdigest()


def iter_files(paths: tuple[str, ...], *, root: Path = DEFAULT_ROOT) -> list[Path]:
    files: list[Path] = []
    for raw in paths:
        if any(ch in raw for ch in "*?[]"):
            files.extend(p for p in root.glob(raw) if p.is_file())
            continue
        path = root / raw
        if path.is_dir():
            files.extend(p for p in path.rglob("*") if p.is_file())
        elif path.is_file():
            files.append(path)
    return sorted(files)


def _rel(path: Path, root: Path) -> str:
    return path.relative_to(root).as_posix()


def _is_ignored(path: Path, root: Path) -> bool:
    rel = _rel(path, root)
    parts = set(Path(rel).parts)
    return (
        bool(parts & set(IGNORED_PATHS))
        or path.suffix.lower() in IGNORED_SUFFIXES
        or rel.endswith(".egg-info/PKG-INFO")
    )


def _is_allowed(path: Path, root: Path) -> bool:
    rel = _rel(path, root)
    return any(
        rel == allowed or rel.startswith(f"{allowed}/")
        for allowed in QUARANTINE_ALLOWLIST
    )


def _raw_scan(
    paths: tuple[str, ...] = SCAN_ROOTS,
    *,
    root: Path = DEFAULT_ROOT,
) -> list[Finding]:
    findings: list[Finding] = []
    for path in iter_files(paths, root=root):
        if _is_ignored(path, root) or _is_allowed(path, root):
            continue
        text = path.read_text(encoding="utf-8", errors="ignore")
        for line_no, line in enumerate(text.splitlines(), start=1):
            line_hash = hashlib.sha256(line.encode("utf-8")).hexdigest()[:16]
            for pattern in SOURCE_COMPANY_PATTERNS:
                match = pattern.search(line)
                if match:
                    findings.append(
                        Finding(path, line_no, "source-residue", match.group(0), line_hash)
                    )
            for pattern in WRONG_DOMAIN_PATTERNS:
                match = pattern.search(line)
                if match:
                    findings.append(
                        Finding(path, line_no, "wrong-domain", match.group(0), line_hash)
                    )
            for pattern in META_RESIDUE_PATTERNS:
                match = pattern.search(line)
                if match:
                    findings.append(
                        Finding(path, line_no, "meta-residue", match.group(0), line_hash)
                    )
            for _ in range(_digest_hit_count(line, DIGEST_DENYLIST)):
                # DIGEST_DENYLIST re-read from module globals here (not a
                # bound default) so a test can monkeypatch the module-level
                # name and have it actually take effect at call time.
                # match is deliberately a fixed redaction, never the actual
                # matched text -- see _digest_hit_count's docstring.
                findings.append(Finding(path, line_no, "digest-residue", "[redacted]", line_hash))
    return findings


def load_baseline(path: Path = BASELINE_PATH) -> set[str]:
    if not path.exists():
        return set()
    return {
        line.strip()
        for line in path.read_text(encoding="utf-8").splitlines()
        if line.strip() and not line.startswith("#")
    }


def scan(
    paths: tuple[str, ...] = SCAN_ROOTS,
    *,
    root: Path = DEFAULT_ROOT,
    use_baseline: bool = True,
) -> list[Finding]:
    findings = _raw_scan(paths, root=root)
    if not use_baseline or root != DEFAULT_ROOT:
        return findings
    baseline = load_baseline()
    return [finding for finding in findings if finding.baseline_key(root) not in baseline]


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("paths", nargs="*", help="Optional repo-relative paths to scan")
    parser.add_argument(
        "--refresh-baseline",
        action="store_true",
        help="Write the current broad scan findings as the known inherited baseline",
    )
    args = parser.parse_args(argv)
    paths = tuple(args.paths) if args.paths else SCAN_ROOTS
    if args.refresh_baseline:
        findings = _raw_scan(SCAN_ROOTS)
        keys = sorted({finding.baseline_key() for finding in findings})
        BASELINE_PATH.write_text(
            "# Known inherited hygiene finding fingerprints. Do not add to this file for new CSM code.\n"
            + "\n".join(keys)
            + ("\n" if keys else ""),
            encoding="utf-8",
        )
        print(f"wrote {BASELINE_PATH.relative_to(DEFAULT_ROOT)} ({len(keys)} findings)")
        return 0
    findings = scan(paths)
    for finding in findings:
        rel = finding.path.relative_to(DEFAULT_ROOT)
        print(f"{rel}:{finding.line_no}: {finding.kind}: {finding.match}")
    return 1 if findings else 0


if __name__ == "__main__":
    raise SystemExit(main())
